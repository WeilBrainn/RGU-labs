#include <stdio.h>

int main()
{
  unsigned int a;
  int byte1, byte2;

  // Ввод числа от пользователя
  printf("Введите 32-битное целое число (в десятичном формате): ");
  scanf("%u", &a);

  // Ввод индексов байтов для обмена
  printf("Введите номер первого байта (0-3): ");
  scanf("%d", &byte1);
  printf("Введите номер второго байта (0-3): ");
  scanf("%d", &byte2);

  // Проверка, чтобы номера байтов были валидными
  if (byte1 < 0 || byte1 > 3 || byte2 < 0 || byte2 > 3)
  {
    printf("Номера байтов должны быть в пределах от 0 до 3.\n");
    return 1; // Завершение программы с ошибкой
  }

  // Меняем местами байты
  unsigned int mask1 = 0xFF << (byte1 * 8); // Маска для первого байта
  unsigned int mask2 = 0xFF << (byte2 * 8); // Маска для второго байта

  // Получаем значения байтов
  unsigned int byteValue1 = (a & mask1) >> (byte1 * 8);
  unsigned int byteValue2 = (a & mask2) >> (byte2 * 8);

  // Удаляем старые байты из числа
  a &= ~mask1; // Убираем первый байт
  a &= ~mask2; // Убираем второй байт

  // Устанавливаем байты на новые позиции
  a |= (byteValue1 << (byte2 * 8)); // Устанавливаем первый байт на позицию второго
  a |= (byteValue2 << (byte1 * 8)); // Устанавливаем второй байт на позицию первого

  // Вывод результата
  printf("Число после обмена байтами: %u\n", a);

  return 0;
}
