#include <stdio.h>

int main()
{
  int a, k;

  // Ввод числа от пользователя
  printf("Введите число a: ");
  scanf("%d", &a);

  // Ввод номера бита от пользователя
  printf("Введите номер бита k: ");
  scanf("%d", &k);

  // Проверка, чтобы номер бита был валидным
  if (k < 0 || k >= sizeof(int) * 8)
  {
    printf("Номер бита k должен быть в пределах от 0 до %d.\n", sizeof(int) * 8 - 1);
    return 1; // Завершение программы с ошибкой
  }

  // Снятие k-ого бита
  a = a & ~(1 << k); // Создаем маску с 0 на позиции k и применяем AND

  // Вывод результата
  printf("Число после снятия k-ого бита: %d\n", a);

  return 0;
}
