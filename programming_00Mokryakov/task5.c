#include <stdio.h>

int main()
{
  int a, i, j;

  // Ввод числа от пользователя
  printf("Введите число a: ");
  scanf("%d", &a);

  // Ввод индексов битов от пользователя
  printf("Введите номер i: ");
  scanf("%d", &i);
  printf("Введите номер j: ");
  scanf("%d", &j);

  // Проверка, чтобы номера битов были валидными
  if (i < 0 || i >= sizeof(int) * 8 || j < 0 || j >= sizeof(int) * 8)
  {
    printf("Номера битов должны быть в пределах от 0 до %d.\n", sizeof(int) * 8 - 1);
    return 1; // Завершение программы с ошибкой
  }

  // Получение значений i-ого и j-ого битов
  int bit_i = (a >> i) & 1; // Получаем i-ый бит
  int bit_j = (a >> j) & 1; // Получаем j-ый бит

  // Если биты разные, меняем их местами
  if (bit_i != bit_j)
  {
    a ^= (1 << i); // Снимаем i-ый бит
    a ^= (1 << j); // Снимаем j-ый бит
  }

  // Вывод результата
  printf("Число после обмена битами: %d\n", a);

  return 0;
}
