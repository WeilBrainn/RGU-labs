#include <stdio.h>

int main()
{
  unsigned int n;

  // Ввод 2^p разрядного числа от пользователя
  printf("Введите 2^p разрядное целое число: ");
  scanf("%u", &n);

  // Переменная для хранения результата XOR
  int result = 0;

  // Проходим по всем 32 битам (или менее, если p < 32)
  for (int i = 0; i < sizeof(n) * 8; i++)
  {
    // Проверяем, является ли i-тый бит установленным
    if (n & (1 << i))
    {
      result ^= 1; // XOR с 1, если бит установлен
    }
  }

  // Вывод результата
  printf("Результат поксора всех битов: %d\n", result);

  return 0;
}
