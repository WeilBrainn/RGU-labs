#include <stdio.h>

int main()
{
  int a, k;

  // Ввод числа от пользователя
  printf("Введите число a: ");
  scanf("%d", &a);

  // Ввод количества обнуляемых битов от пользователя
  printf("Введите количество младших бит k для обнуления: ");
  scanf("%d", &k);

  // Проверка, чтобы количество битов было валидным
  if (k < 0 || k >= sizeof(int) * 8)
  {
    printf("Количество бит k должно быть в пределах от 0 до %d.\n", sizeof(int) * 8 - 1);
    return 1; // Завершение программы с ошибкой
  }

  // Создание маски с 0 на младших k битах
  int mask = ~((1 << k) - 1); // Сдвигаем 1 влево на k и вычитаем 1, затем инвертируем

  // Обнуление младших k бит
  a = a & mask; // Применяем AND с маской

  // Вывод результата
  printf("Число после обнуления младших k бит: %d\n", a);

  return 0;
}
